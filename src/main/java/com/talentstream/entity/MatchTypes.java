package com.talentstream.entity;

public class MatchTypes {
	
	private String name;
    private String email;
    private String mobilenumber;
    private String jobTitle;
    private String applicantStatus;
    private String minimumExperience;
    private String skillName;
    private String minimumQualification;
    private String location;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobilenumber() {
		return mobilenumber;
	}
	public void setMobilenumber(String mobilenumber) {
		this.mobilenumber = mobilenumber;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getApplicantStatus() {
		return applicantStatus;
	}
	public void setApplicantStatus(String applicantStatus) {
		this.applicantStatus = applicantStatus;
	}
	
	public String getMinimumExperience() {
		return minimumExperience;
	}
	public void setMinimumExperience(String minimumExperience) {
		this.minimumExperience = minimumExperience;
	}
	public String getSkillName() {
		return skillName;
	}
	public void setSkillName(String skillName) {
		this.skillName = skillName;
	}
	public String getMinimumQualification() {
		return minimumQualification;
	}
	public void setMinimumQualification(String minimumQualification) {
		this.minimumQualification = minimumQualification;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
    
    

}
